#include "header.h"
#include <stdio.h>

void aloneCan(int *arr, int n) {
    int val = 0;
    for (int i = 0; i < n; i++)
        val ^=arr[i];
    for (int i = 0; i < n; i++) {
        if ((arr[i] ^ val) == 0)
            printf("%d\n", arr[i]);
    }
}
