#include "header.h"

char *getXor(char *a, char *b) {
    for (int i = 0; i < 6; i++) {
        if ((a[i] == '0' && b[i] == '0') || (a[i] == '1' && b[i] == '1'))
            a[i] = '0';
        else
            a[i] = '1';
    }
    return a;
}

int toInt(char *bits) {
    int sum = 0;
    for (int i = 5, j = 0; i >= 0; i--, j++) {
        if (bits[i] == '0')
            ;
        else {
            sum += (1 << j);
        }
    }
    return sum;
}
